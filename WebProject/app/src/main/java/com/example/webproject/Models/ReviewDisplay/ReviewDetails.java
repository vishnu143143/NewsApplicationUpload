package com.example.webproject.Models.ReviewDisplay;

public class ReviewDetails {
    int id;
    String name;
    String description;
    float rating;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getDescription() {
        return description;
    }

    public float getRating() {
        return rating;
    }
}
